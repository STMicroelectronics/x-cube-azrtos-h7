static void tcp_listen_callback(NX_TCP_SOCKET *socket, UINT port)
{
  DEBUG_TCP_SOCKET_STATE(socket);

  /* Incoming connection, accept and queuing new requests. */
  nx_tcp_server_socket_accept(socket, NX_NO_WAIT);
  nx_tcp_server_socket_unlisten(&IpInstance, port);

#ifdef ENABLE_IOT_DEBUG
  {
    ULONG peer_ip;
    ULONG peer_port;
    uint8_t peer_addr[4];

    nx_tcp_socket_peer_info_get(socket, &peer_ip, &peer_port);

    peer_addr[0] = (uint8_t)(peer_ip >> 24);
    peer_addr[1] = (uint8_t)(peer_ip >> 16);
    peer_addr[2] = (uint8_t)(peer_ip >> 8);
    peer_addr[3] = (uint8_t)(peer_ip);

    MSG_DEBUG("\n[TCP %p] INCOMING CONNECTION from %02d.%02d.%02d.%02d:%"PRIu32"\n\n",
              socket,
              peer_addr[0], peer_addr[1], peer_addr[2], peer_addr[3], (uint32_t)peer_port);
  }
#endif /* ENABLE_IOT_DEBUG */

  /* Attempt to find another idle socket to start listening on */
  http_server_start_listening();
}


/*
 * This callback is invoked when data is already received.
 * Retrieving packet with no suspension.
 */
static void tcp_receive_notify(NX_TCP_SOCKET *socket)
{
  ClientInfosTypeDef peer;
  peer.tcp_socket = socket;
  peer.info[0] = '\0';
  peer.err = 0;
  peer.status = STATUS_NEW;
  peer.message = 0;
  peer.thread_name[0] = '\0';

  DEBUG_TCP_SOCKET_STATE(socket);

  {
    ULONG peer_ip;
    ULONG peer_port;
    uint8_t peer_addr[4];
    NX_PACKET *packet;

    nx_tcp_socket_receive(socket, &packet, NX_NO_WAIT);

    nx_tcp_socket_peer_info_get(socket, &peer_ip, &peer_port);

    peer_addr[0] = (uint8_t)(peer_ip >> 24);
    peer_addr[1] = (uint8_t)(peer_ip >> 16);
    peer_addr[2] = (uint8_t)(peer_ip >> 8);
    peer_addr[3] = (uint8_t)(peer_ip);

    MSG_DEBUG("\n[TCP %p] INCOMING PACKET (%"PRIu32" bytes) from %02d.%02d.%02d.%02d:%"PRIu32"\n\n",
              socket, (uint32_t)packet->nx_packet_length,
              peer_addr[0], peer_addr[1], peer_addr[2], peer_addr[3], (uint32_t)peer_port);

    snprintf(peer.info, sizeof(peer.info),
             "%02d.%02d.%02d.%02d:%"PRIu32"",
             peer_addr[0], peer_addr[1], peer_addr[2], peer_addr[3], (uint32_t)peer_port);

    const char *const recv_buffer = (const char *)packet->nx_packet_prepend_ptr;
    /* MSG_DEBUG("recv_buffer : %s\n", recv_buffer); */

    if (strlen(recv_buffer) == 0)
    {
      UINT done = nx_packet_release(packet);
      if (done != NX_SUCCESS)
      {
        MSG_ERROR("[TCP %p] packet release failed\n", peer.tcp_socket);
      }
      close_client(&peer);
      MSG_ERROR("\n");
      return;
    }

    /* Check if request to get favicon.ico */
    /* GET /favicon.ico HTTP/1.1 */
    /* Host: xx.xx.xxx.xxx:80 */
    /* Connection: keep-alive */
    /* Pragma: no-cache */
    /* Cache-Control: no-cache */
    /* User-Agent: Mozilla/5.0 (Windows NT 10.0; WOW64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/83.0.41... */
    /* Accept: image/webp,image/apng,image... */
    /* Referer: http://xx.xx.xxx.xxx:80/ */
    /* Accept-Encoding: gzip, deflate */
    /* Accept-Language: en-US,en;q=0.9 */
    if (strncmp(recv_buffer, "GET /favicon.ico", 16) == 0)
    {
      UINT done = nx_packet_release(packet);
      if (done != NX_SUCCESS)
      {
        MSG_ERROR("[TCP %p] packet release failed\n", peer.tcp_socket);
      }
      http_send_favicon_png(&peer);
    }
    /* Check if request to get ST_logo_2020_white_no_tagline.png */
    else if (strncmp(recv_buffer, "GET /ST_logo_2020_white_no_tagline.png", 38) == 0)
    {
      UINT done = nx_packet_release(packet);
      if (done != NX_SUCCESS)
      {
        MSG_ERROR("[TCP %p] packet release failed\n", peer.tcp_socket);
      }
      http_send_st_logo_png(&peer);
    }
    else if (strncmp(recv_buffer, "GET / ", 6) == 0)
    {
      const char *request_reader = &recv_buffer[6];
      int32_t event_stream = 0;

      while ((request_reader != &recv_buffer[packet->nx_packet_length]) && (event_stream == 0))
      {
        if (strncmp(request_reader, "Accept: text/event-stream", 25) == 0)
        {
          event_stream = 1;
        }
        else
        {
          request_reader++;
        }
      }

      UINT done = nx_packet_release(packet);
      if (done != NX_SUCCESS)
      {
        MSG_ERROR("[TCP %p] packet release failed\n", peer.tcp_socket);
      }

      if (event_stream)
      {
        /* GET / HTTP/1.1 */
        /* Host: xx.xx.xxx.xxx:80 */
        /* Connection: keep-alive */
        /* Accept: text/event-stream */
        http_send_event_stream(&peer);
      }
      else
      {
        /* GET / HTTP/1.1 */
        /* Host: xx.xx.xxx.xxx:80 */
        /* Connection: keep-alive */
        /* Upgrade-Insecure-Requests: 1 */
        /* User-Agent: Mozilla/5.0 (Windows NT 10.0; WOW64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/83.0.41... */
        /* Accept: text/html,application/xhtml+xml,application/xml;q=0.9,image/webp,image/apng, ... */
        /* Accept-Encoding: gzip, deflate */
        /* Accept-Language: en-US,en;q=0.9 */
        {
          http_send_index_html(&peer);
        }
      }
    }
    else
    {
      UINT done = nx_packet_release(packet);
      if (done != NX_SUCCESS)
      {
        MSG_ERROR("[TCP %p] packet release failed\n", peer.tcp_socket);
      }
      http_send_error_404_html(&peer);
      return;
    }
  }

  static ClientInfosTypeDef ClientObjetcs[MAX_NUM_OF_CLIENTS];

  /* This peer can become a connected client which is ready to handle events. */
  if (peer.status == STATUS_COMM_STREAM)
  {
    /* Search which index is free to create a new client. */
    uint32_t i;
    for (i = 0; i < MAX_NUM_OF_CLIENTS; i++)
    {
      if (ClientObjetcs[i].status == STATUS_UNKNOWN)
      {
        break;
      }
    }

    if (i == MAX_NUM_OF_CLIENTS)
    {
      MSG_INFO("Too many clients already connected\n");
      return;
    }
    else
    {
      VOID *stack;
      const ULONG stack_size = HTTP_CHILD_TASK_STACK_SIZE;

      /* Allocate the memory for HTTP SSE thread */
      if (tx_byte_allocate(BytePool, &stack, stack_size, TX_NO_WAIT) != TX_SUCCESS)
      {
        MSG_ERROR("tx_byte_allocate() failed\n");
        return;
      }


      /* Make this client persistent. */
      /* Copy all information from the peer. */
      ClientObjetcs[i] = peer;

      /* Create the HTTP SSE Thread for this client. */
      const size_t thread_name_len = sizeof(ClientObjetcs[i].thread_name);

      ClientInfosTypeDef *const p = &ClientObjetcs[i];

      TX_THREAD *const thread = &p->thread;
      snprintf(p->thread_name, thread_name_len, "HTTP_%08"PRIX32, i);
      p->thread_name[thread_name_len - 1] = '\0';

      UINT status = tx_thread_create(&p->thread, p->thread_name, sse_thread_entry,
                                     (ULONG)p, stack, stack_size,
                                     HTTP_CHILD_THREAD_PRIO, HTTP_CHILD_THREAD_PRIO,
                                     HTTP_CHILD_TASK_TIME_SLICE, TX_DONT_START);
      if (status)
      {
        MSG_ERROR("Unable to create HTTP SSE Thread\n");
        Error_Handler();
      }

      status = tx_thread_entry_exit_notify(thread, sse_thread_exit_notify);

      tx_thread_resume(thread);
    }
  }
}

static void sse_thread_entry(ULONG arg)
{
  ClientInfosTypeDef *client = (ClientInfosTypeDef *)(arg);
  const ULONG ticksFor500ms = (1 * TX_TIMER_TICKS_PER_SECOND) / 2;

  while (client->status == STATUS_COMM_STREAM)
  {
    client_infos(client);

    tx_thread_sleep(ticksFor500ms);

    /* If connection is ok, sent events for this client. */
    {
      char event_string[2000] = "";
      task_list(event_string);
      http_send_event(client, event_string);
    }

    if (client->status == STATUS_COMM_STREAM)
    {
      tx_thread_sleep(4 * ticksFor500ms);
      char event_string[70] = "";
      get_uptime(event_string);
      http_send_event(client, event_string);
    }

    if (client->status == STATUS_COMM_STREAM)
    {
      tx_thread_sleep(3 * ticksFor500ms);
    }
  }

  client_infos(client);
}


/*
 * If status is TX_SUCCESS the entry/exit notification function was
 * successfully registered.
 */
static void sse_thread_exit_notify(TX_THREAD *thread, UINT condition)
{
  /* Determine if the thread was entered or exited. */
  if (condition == TX_THREAD_EXIT)
  {
    /* Thread exit! */
    UINT status = tx_byte_release(thread->tx_thread_stack_start);
    if (status)
    {
      MSG_ERROR("tx_byte_release failed for: %p\n", thread);
      Error_Handler();
    }

    status = tx_thread_delete(thread);
    if (status)
    {
      MSG_ERROR("Unable to delete HTTP SSE Thread: %p\n", thread);
      Error_Handler();
    }

    ClientInfosTypeDef *client = (ClientInfosTypeDef *)(thread->tx_thread_entry_parameter);
    client->status = STATUS_UNKNOWN;

    client_infos(client);
  }
}

static void task_list(char *buffer_string)
{
  extern ULONG _tx_thread_created_count;
  extern TX_THREAD *_tx_thread_created_ptr;

  const uint32_t current_number_of_threads = (uint32_t) _tx_thread_created_count;
  const TX_THREAD *thread_ptr = _tx_thread_created_ptr;
  static const char *to_state_str[] =
  {
    "READY          ",
    "COMPLETED      ",
    "TERMINATED     ",
    "SUSPENDED      ",
    "SLEEP          ",
    "QUEUE_SUSP     ",
    "SEMAPHORE_SUSP ",
    "EVENT_FLAG     ",
    "BLOCK_MEMORY   ",
    "BYTE_MEMORY    ",
    "IO_DRIVER      ",
    "FILE           ",
    "TCP_IP         ",
    "MUTEX_SUSP     ",
    "PRIORITY_CHANGE"
  };

  sprintf(buffer_string, "%-25s %-15s Prio Stack Start Stack End Size  Stack Ptr\n", "Name", "State");
  strcat(buffer_string, "--------------------------------------------------------------------------------------\n");

  /* The list of tasks and their status. */
  for (uint32_t count = 0; count < current_number_of_threads; count++)
  {
    sprintf(buffer_string + strlen(buffer_string), "%-25s %-15s %3"PRIu32"  %p %p %"PRIu32" %p\r\n",
            thread_ptr->tx_thread_name,
            to_state_str[thread_ptr->tx_thread_state],
            (uint32_t)thread_ptr->tx_thread_priority,
            thread_ptr->tx_thread_stack_start,
            thread_ptr->tx_thread_stack_end,
            (uint32_t)thread_ptr->tx_thread_stack_size,
            thread_ptr->tx_thread_stack_ptr);

    thread_ptr = thread_ptr->tx_thread_created_next;
  }

  strcat(buffer_string, "--------------------------------------------------------------------------------------\n");
}


static void get_uptime(char *buffer_string)
{
  char uptime[60] = {0};
  const uint32_t milliseconds = HAL_GetTick();

  uint32_t seconds = (uint32_t)(milliseconds / 1000) % 60 ;
  uint32_t minutes = (uint32_t)((milliseconds / (1000 * 60)) % 60);
  uint32_t hours   = (uint32_t)((milliseconds / (1000 * 60 * 60)) % 24);

  snprintf(uptime, sizeof(uptime), "UP Time is : %"PRIu32" hour(s) : %"PRIu32" min(s) : %"PRIu32" secs",
           hours, minutes, seconds);
  strcat(buffer_string, uptime);
}


static int32_t http_send_event(ClientInfosTypeDef *client, char *buffer_string)
{
  if (strlen(buffer_string) == 0)
  {
    return 1;
  }

  if (client->status == STATUS_COMM_STREAM)
  {
    char send_outbuffer[2300];

    client->message++;

    MSG_DEBUG("[TCP %p] ->Send to %s\n", client->tcp_socket, client->info);
    http_chunk_encode(send_outbuffer, sizeof(send_outbuffer), buffer_string);

    if (http_server_write(client, send_outbuffer, strlen(send_outbuffer)))
    {
      MSG_DEBUG("Close client which signed error at send:\n");
      close_client(client);
    }
  }
  else
  {
    MSG_DEBUG("#### EVENT NOT SENT ####\n");
  }

  return 0;
}

static int32_t http_chunk_encode(char *out_buffer, size_t out_buffer_len, char *message)
{
  char *line;
  static long id_counter = 0;

  char http_chunk_encoding_idbuffer[50];
  char http_chunk_encoding_databuffer[2000];

  http_chunk_encoding_idbuffer[0] = '\0';
  http_chunk_encoding_databuffer[0] = '\0';

  snprintf(http_chunk_encoding_idbuffer, sizeof(http_chunk_encoding_idbuffer), "id: %ld\n", id_counter++);

  chop(message);

  char *strtok_r_worker;
  line = strtok_r(message, "\n", &strtok_r_worker);

  while (line != NULL)
  {
    chop(line);
    /* MSG_DEBUG("*** %s\n", line); */

    strcat(http_chunk_encoding_databuffer, "data: ");
    strcat(http_chunk_encoding_databuffer, line);
    strcat(http_chunk_encoding_databuffer, "\n");

    line = strtok_r(NULL, "\n", &strtok_r_worker);
  }

  strcat(http_chunk_encoding_databuffer, "\n");

  snprintf(out_buffer, out_buffer_len,
           "Content-Type: text/event-stream; charset=utf8\r\n"
           /* "Transfer-Encoding: chunked\r\n" */
           "%x\r\n%s%s\n\n\r\n", (unsigned int)
           (strlen(http_chunk_encoding_idbuffer) + strlen(http_chunk_encoding_databuffer) + 2), /* +2 is the two \n */
           http_chunk_encoding_idbuffer,
           http_chunk_encoding_databuffer);

  return 0;
}


static char *chop(char *str)
{
  char *c = str;
  c += strlen(str) - 1;

  while ((c != str) && ((*c == '\n') || (*c == '\r')))
  {
    *c = '\0';
    --c;
  }

  return str;
}


